    /*
    输入：n = 1010 (十进制 10)
    目标输出：0101 (十进制 5)
    初始状态：res = 0000, n = 1010
    第 1 次循环：提取 n 的最低位：bit = n & 1  -->  1010 & 0001 = 0
    组装进 res：res = (res << 1) | bit  -->  (0000 << 1) | 0 = 0000n
    无符号右移：n >>>= 1  -->  0101
    第 2 次循环：提取最低位：bit = n & 1  -->  0101 & 0001 = 1
    组装进 res：res = (res << 1) | bit  -->  (0000 << 1) | 1 = 0001
    n 无符号右移：n >>>= 1  -->  0010
    第 3 次循环：提取最低位：bit = n & 1  -->  0010 & 0001 = 0
    组装进 res：res = (res << 1) | bit  -->  (0001 << 1) | 0 = 0010
    n 无符号右移：n >>>= 1  -->  0001
    第 4 次循环：提取最低位：bit = n & 1  -->  0001 & 0001 = 1
    组装进 res：res = (res << 1) | bit  -->  (0010 << 1) | 1 = 0101
    n 无符号右移：n >>>= 1  -->  0000
    结束。
    结果 res = 0101。
     */
    // leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int reverseBits(int n) {
            int res = 0;
            // 题目明确是一个 32 位的整数，因此固定循环 32 次
            for (int i = 0; i < 32; i++) {
                // 1. 将 res 左移一位，为新来的位腾出空间
                // 注意：第一次循环时 res 为 0，左移依然是 0
                res <<= 1;

                // 2. 提取 n 的最低位，并与 res 进行按位或拼接
                res |= (n & 1);

                // 3. 将 n 无符号右移一位，丢弃已经处理过的最低位
                // 重点：必须使用 >>> 无符号右移，防止负数产生无限的符号位扩展
                n >>>= 1;
            }
            return res;
        }
    }
